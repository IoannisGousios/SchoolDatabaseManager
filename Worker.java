public class Worker {
	
	private int id;
	private String name;
	private String surname;
	private double weekSal;
	private double hoursPerDay;
	private double hourSal;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public double getWeekSal() {
		return weekSal;
	}
	public void setWeekSal(double weekSal) {
		this.weekSal = weekSal;
	}
	public double getHoursPerDay() {
		return hoursPerDay;
	}
	public void setHoursPerDay(double hoursPerDay) {
		this.hoursPerDay = hoursPerDay;
	}
	public double getHourSal() {
		return hourSal;
	}
	public void setHourSal(double hourSal) {
		this.hourSal = hourSal;
	}
	
	

}
